"""BirdClef 2026 — Experiment Results Analyzer

Reads all result.json files produced by train.py and prints a structured
comparison table.  Run this after experiments finish, then share the output
with Claude so it can suggest next steps and update the codebase.

Usage:
    python analyze_results.py                      # reads outputs/ recursively
    python analyze_results.py --dir outputs/debug  # specific directory
    python analyze_results.py --top 10             # show top-N runs only
    python analyze_results.py --update-strong      # also rewrite run_strong.sh
"""

import argparse
import json
import os
import glob
from typing import Any


# ── CLI ──────────────────────────────────────────────────────────────────────

def parse_args():
    parser = argparse.ArgumentParser(description="Analyze BirdClef experiment results")
    parser.add_argument("--dir", default="outputs", help="Root directory to search")
    parser.add_argument("--top", type=int, default=None, help="Show only top-N runs")
    parser.add_argument("--update-strong", action="store_true",
                        help="Rewrite scripts/run_strong.sh with best hyperparameters")
    return parser.parse_args()


# ── Loading ──────────────────────────────────────────────────────────────────

def load_all_results(root: str) -> list[dict]:
    pattern = os.path.join(root, "**", "result.json")
    paths = glob.glob(pattern, recursive=True)
    results = []
    for path in paths:
        with open(path) as f:
            data = json.load(f)
        data["_path"] = path
        results.append(data)
    return results


# ── Formatting ───────────────────────────────────────────────────────────────

def fmt(v: Any, width: int = 10) -> str:
    if v is None:
        return "-".ljust(width)
    if isinstance(v, float):
        return f"{v:.4f}".ljust(width)
    return str(v).ljust(width)


def _bar(value: float, best: float, width: int = 20) -> str:
    if best == 0:
        return " " * width
    filled = round(value / best * width)
    return "█" * filled + "░" * (width - filled)


# ── Sensitivity analysis ──────────────────────────────────────────────────────

def sensitivity_analysis(results: list[dict]) -> dict[str, dict]:
    """
    For each hyperparameter, measure how much val_cmap varies across its values.
    Returns {hparam: {value: best_cmap, ...}} sorted by range (most impactful first).
    """
    hparam_map: dict[str, dict] = {}

    for r in results:
        cmap = r.get("best_val_cmap", 0.0)
        for k, v in r.get("hparams", {}).items():
            hparam_map.setdefault(k, {})
            key = str(v)
            if key not in hparam_map[k] or cmap > hparam_map[k][key]:
                hparam_map[k][key] = cmap

    # Compute range for each hparam (only if it actually varies)
    sensitivity: dict[str, dict] = {}
    for k, value_map in hparam_map.items():
        if len(value_map) < 2:
            continue
        cmaps = list(value_map.values())
        rng = max(cmaps) - min(cmaps)
        sensitivity[k] = {"values": value_map, "range": rng,
                           "best_value": max(value_map, key=lambda x: value_map[x])}

    return dict(sorted(sensitivity.items(), key=lambda x: -x[1]["range"]))


# ── Learning curve helpers ────────────────────────────────────────────────────

def convergence_epoch(history: list[dict]) -> int | None:
    """Return the epoch where the run first achieved within 1% of its best cMAP."""
    if not history:
        return None
    best = max(h["val_cmap"] for h in history)
    threshold = best * 0.99
    for h in history:
        if h["val_cmap"] >= threshold:
            return h["epoch"]
    return None


def is_overfitting(history: list[dict], window: int = 5) -> bool:
    """True if val_cmap declined in the last `window` epochs while loss kept dropping."""
    if len(history) < window + 1:
        return False
    recent_cmap = [h["val_cmap"] for h in history[-window:]]
    recent_loss = [h["train_loss"] for h in history[-window:]]
    return recent_cmap[-1] < recent_cmap[0] and recent_loss[-1] < recent_loss[0]


# ── run_strong.sh updater ────────────────────────────────────────────────────

STRONG_TEMPLATE = """\
#!/usr/bin/env bash
# =============================================================================
# Strong Run — auto-generated by analyze_results.py
# Best hyperparameters identified from sweep experiments.
# =============================================================================
set -e
cd "$(dirname "$0")/.."

BEST_LR="{lr}"
BEST_MIXUP="{mixup_alpha}"
BEST_DIM="{hidden_dim}"
BEST_DROPOUT="{dropout}"
BEST_RATING="{min_rating}"
BEST_SEC_LABELS="{use_secondary_labels}"
BEST_CLIPS="{n_clips_per_file}"

echo ""
echo "============================================================"
echo "  Strong run (auto-generated)"
echo "  lr=${{BEST_LR}}  mixup=${{BEST_MIXUP}}  dim=${{BEST_DIM}}"
echo "  dropout=${{BEST_DROPOUT}}  min_rating=${{BEST_RATING}}"
echo "  secondary=${{BEST_SEC_LABELS}}  clips=${{BEST_CLIPS}}"
echo "============================================================"

python train.py \\
    --config configs/default.yaml \\
    experiment.name="strong_run_auto" \\
    training.learning_rate="${{BEST_LR}}" \\
    training.mixup_alpha="${{BEST_MIXUP}}" \\
    model.hidden_dim="${{BEST_DIM}}" \\
    model.dropout="${{BEST_DROPOUT}}" \\
    data.min_rating="${{BEST_RATING}}" \\
    data.use_secondary_labels="${{BEST_SEC_LABELS}}" \\
    audio.n_clips_per_file="${{BEST_CLIPS}}" \\
    training.epochs="80"

echo ""
echo "Strong run complete."
"""

def update_strong_script(best_hparams: dict, path: str = "scripts/run_strong.sh") -> None:
    content = STRONG_TEMPLATE.format(
        lr=best_hparams.get("learning_rate", "1e-3"),
        mixup_alpha=best_hparams.get("mixup_alpha", "0.3"),
        hidden_dim=best_hparams.get("hidden_dim", 512),
        dropout=best_hparams.get("dropout", 0.3),
        min_rating=best_hparams.get("min_rating", 0.0),
        use_secondary_labels=str(best_hparams.get("use_secondary_labels", True)).lower(),
        n_clips_per_file=best_hparams.get("n_clips_per_file", 3),
    )
    with open(path, "w") as f:
        f.write(content)
    os.chmod(path, 0o755)
    print(f"\nrun_strong.sh updated → {path}")


# ── Main ─────────────────────────────────────────────────────────────────────

def main():
    args = parse_args()
    results = load_all_results(args.dir)

    if not results:
        print(f"No result.json files found under '{args.dir}'.")
        print("Run some experiments first, then re-run this script.")
        return

    # Sort by best val_cmap descending
    results.sort(key=lambda r: r.get("best_val_cmap", 0.0), reverse=True)
    if args.top:
        results = results[:args.top]

    best_overall = results[0]["best_val_cmap"]

    # ── Rankings table ────────────────────────────────────────────────────────
    print("\n" + "=" * 100)
    print("EXPERIMENT RANKINGS  (sorted by best val padded-cMAP)")
    print("=" * 100)
    header = (
        f"{'Rank':<5} {'Run name':<35} {'cMAP':>7} {'Best ep':>8} "
        f"{'Epochs':>7} {'Done':>5}  Progress"
    )
    print(header)
    print("-" * 100)

    for rank, r in enumerate(results, 1):
        cmap = r.get("best_val_cmap", 0.0)
        bar = _bar(cmap, best_overall, width=25)
        done = "✓" if r.get("finished") else "…"
        print(
            f"{rank:<5} {r['run_name']:<35} {cmap:>7.4f} "
            f"{r.get('best_epoch', '?'):>8} "
            f"{r.get('total_epochs_run', '?'):>7} "
            f"{done:>5}  {bar}"
        )

    # ── Sensitivity analysis ──────────────────────────────────────────────────
    all_results_full = load_all_results(args.dir)   # untruncated for analysis
    sens = sensitivity_analysis(all_results_full)

    if sens:
        print("\n" + "=" * 100)
        print("HYPERPARAMETER SENSITIVITY  (most impactful → least impactful)")
        print("=" * 100)
        print(f"{'Hyperparameter':<28} {'Impact (cMAP range)':>20}  Best value → best cMAP")
        print("-" * 100)
        for hparam, info in sens.items():
            impact_bar = "█" * min(int(info["range"] * 500), 30)
            best_cmap_for_hparam = info["values"][info["best_value"]]
            print(
                f"{hparam:<28} {info['range']:>10.4f} {impact_bar:<32} "
                f"{info['best_value']} → {best_cmap_for_hparam:.4f}"
            )

    # ── Per-hyperparameter breakdown ──────────────────────────────────────────
    if sens:
        print("\n" + "=" * 100)
        print("PER-HYPERPARAMETER VALUE BREAKDOWN")
        print("=" * 100)
        for hparam, info in list(sens.items())[:6]:   # top-6 most impactful
            print(f"\n  {hparam}")
            sorted_vals = sorted(info["values"].items(), key=lambda x: -x[1])
            for val, cmap in sorted_vals:
                marker = " ← best" if val == info["best_value"] else ""
                bar = _bar(cmap, best_overall, width=20)
                print(f"    {str(val):<12} {cmap:.4f}  {bar}{marker}")

    # ── Overfitting / convergence diagnostics ─────────────────────────────────
    print("\n" + "=" * 100)
    print("LEARNING CURVE DIAGNOSTICS")
    print("=" * 100)
    print(f"{'Run name':<35} {'Converged ep':>13} {'Overfitting?':>14}  {'Avg epoch time':>16}")
    print("-" * 100)
    for r in all_results_full:
        history = r.get("epoch_history", [])
        conv = convergence_epoch(history)
        overfit = is_overfitting(history)
        avg_time = (
            sum(h.get("epoch_time_s", 0) for h in history) / len(history)
            if history else None
        )
        avg_str = f"{avg_time:.1f}s" if avg_time else "-"
        overfit_str = "YES ⚠" if overfit else "no"
        conv_str = str(conv) if conv else "-"
        print(f"{r['run_name']:<35} {conv_str:>13} {overfit_str:>14}  {avg_str:>16}")

    # ── Best config summary ───────────────────────────────────────────────────
    best_run = results[0]
    best_hparams = best_run.get("hparams", {})

    print("\n" + "=" * 100)
    print(f"BEST RUN SUMMARY  →  {best_run['run_name']}  (cMAP={best_overall:.4f})")
    print("=" * 100)
    for k, v in best_hparams.items():
        print(f"  {k:<30} {v}")

    # ── Update run_strong.sh ──────────────────────────────────────────────────
    if args.update_strong:
        update_strong_script(best_hparams)

    # ── Suggestions for Claude ────────────────────────────────────────────────
    print("\n" + "=" * 100)
    print("STRUCTURED SUMMARY FOR CLAUDE")
    print("=" * 100)
    summary = {
        "n_runs_analyzed": len(all_results_full),
        "best_run": best_run["run_name"],
        "best_val_cmap": best_overall,
        "best_hparams": best_hparams,
        "sensitivity": {k: {"range": v["range"], "best_value": v["best_value"]}
                        for k, v in sens.items()},
        "overfitting_runs": [r["run_name"] for r in all_results_full
                             if is_overfitting(r.get("epoch_history", []))],
        "top5": [{"run": r["run_name"], "cmap": r["best_val_cmap"]}
                 for r in results[:5]],
    }
    print(json.dumps(summary, indent=2))
    print("\n→  Copy the above JSON and paste it to Claude for analysis.")


if __name__ == "__main__":
    main()
